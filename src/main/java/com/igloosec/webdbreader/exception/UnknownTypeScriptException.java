package com.igloosec.webdbreader.exception;

public class UnknownTypeScriptException extends Exception {

	public UnknownTypeScriptException() {
		super();
	}

	public UnknownTypeScriptException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UnknownTypeScriptException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnknownTypeScriptException(String message) {
		super(message);
	}

	public UnknownTypeScriptException(Throwable cause) {
		super(cause);
	}
	
}
